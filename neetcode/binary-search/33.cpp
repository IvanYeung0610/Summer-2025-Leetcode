// 33. Search in Rotated Sorted Array

#include <vector>

using namespace std;

class Solution {
public:
    int search(vector<int>& nums, int target) {
        int left = 0;
        int right = nums.size();
        
        while (left <= right) {
            int mid = (left + right) / 2;
        }
    }
};
